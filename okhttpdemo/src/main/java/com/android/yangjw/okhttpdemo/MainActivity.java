package com.android.yangjw.okhttpdemo;

import android.content.Intent;
import android.support.v7.app.AppCompatActivity;
import android.os.Bundle;
import android.view.View;
import android.widget.AdapterView;
import android.widget.ListView;

public class MainActivity extends AppCompatActivity {

    private ListView mListView;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        mListView = (ListView) findViewById(R.id.okhttp_index_listview);
        mListView.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
                Intent intent = new Intent();
                switch (position) {
                    case 0:
                        intent.setClass(MainActivity.this,GetSyncActivity.class);
                        break;
                    case 1:
                        intent.setClass(MainActivity.this,GetAsyncActivity.class);
                        break;
                    case 2:
                        intent.setClass(MainActivity.this,PostSyncActivity.class);
                        break;
                    case 3:
                        intent.setClass(MainActivity.this,PostAsyncActivity.class);
                        break;
                    case 4:
                        intent.setClass(MainActivity.this,HttpDemoActivity.class);
                        break;
                    case 5:
                        break;
                }
                startActivity(intent);
            }
        });
    }
}
